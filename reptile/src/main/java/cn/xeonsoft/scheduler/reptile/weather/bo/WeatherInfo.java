package cn.xeonsoft.scheduler.reptile.weather.bo;

public class WeatherInfo {
	private String city;
	private String temp;
	private String tempn;
	private String weather;
	private String wd;
	private String ws;
	private String weathercode;
	private String weathercoden;
	private String fctime;

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getTemp() {
		return temp;
	}

	public void setTemp(String temp) {
		this.temp = temp;
	}

	public String getTempn() {
		return tempn;
	}

	public void setTempn(String tempn) {
		this.tempn = tempn;
	}

	public String getWeather() {
		return weather;
	}

	public void setWeather(String weather) {
		this.weather = weather;
	}

	public String getWd() {
		return wd;
	}

	public void setWd(String wd) {
		this.wd = wd;
	}

	public String getWs() {
		return ws;
	}

	public void setWs(String ws) {
		this.ws = ws;
	}

	public String getWeathercode() {
		return weathercode;
	}

	public void setWeathercode(String weathercode) {
		this.weathercode = weathercode;
	}

	public String getWeathercoden() {
		return weathercoden;
	}

	public void setWeathercoden(String weathercoden) {
		this.weathercoden = weathercoden;
	}

	public String getFctime() {
		return fctime;
	}

	public void setFctime(String fctime) {
		this.fctime = fctime;
	}

}
