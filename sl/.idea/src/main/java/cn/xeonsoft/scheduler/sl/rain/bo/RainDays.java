package cn.xeonsoft.scheduler.sl.rain.bo;

public class RainDays {
	private String stcd;
	private Integer days; // 下雨天数

	public String getStcd() {
		return stcd;
	}

	public void setStcd(String stcd) {
		this.stcd = stcd;
	}

	public Integer getDays() {
		return days;
	}

	public void setDays(Integer days) {
		this.days = days;
	}
}
