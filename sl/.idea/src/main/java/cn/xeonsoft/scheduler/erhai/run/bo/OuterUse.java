package cn.xeonsoft.scheduler.erhai.run.bo;

public class OuterUse {
    private String tm;
    private Float life;
    private Float industry;
    private Float farming;

    public String getTm() {
        return tm;
    }

    public void setTm(String tm) {
        this.tm = tm;
    }

    public Float getLife() {
        return life;
    }

    public void setLife(Float life) {
        this.life = life;
    }

    public Float getIndustry() {
        return industry;
    }

    public void setIndustry(Float industry) {
        this.industry = industry;
    }

    public Float getFarming() {
        return farming;
    }

    public void setFarming(Float farming) {
        this.farming = farming;
    }
}
