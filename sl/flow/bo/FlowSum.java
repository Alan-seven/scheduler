package cn.xeonsoft.scheduler.sl.flow.bo;

import java.util.Date;

public class FlowSum {
    private String stcd;
    private String tm;
    private Float sumq;
    private String type;
    private String sttdrcd;

    public String getStcd() {
        return stcd;
    }

    public void setStcd(String stcd) {
        this.stcd = stcd;
    }

    public String getTm() {
        return tm;
    }

    public void setTm(String tm) {
        this.tm = tm;
    }

    public Float getSumq() {
        return sumq;
    }

    public void setSumq(Float sumq) {
        this.sumq = sumq;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSttdrcd() {
        return sttdrcd;
    }

    public void setSttdrcd(String sttdrcd) {
        this.sttdrcd = sttdrcd;
    }
}
